template <typename UIntType>
inline UIntType GetNextPowerOfTwo(const UIntType word) {
  constexpr UIntType one = static_cast<UIntType>(1);
  return one << ((sizeof(UIntType) << 3) - LeadingZeroCount(word - one));
}

template <typename UIntType>
inline UIntType GetNextPrimeNumber(const UIntType word) {
  const std::uint64_t *begin = kPrimes;
  const std::uint64_t *end = begin + sizeof(kPrimes) / sizeof(kPrimes[0]);

  const std::uint64_t *it = std::upper_bound(begin, end, word);
  DCHECK(it != end);
  DCHECK_LE(*it, static_cast<std::uint64_t>(std::numeric_limits<UIntType>::max()));
  return *it;
}

